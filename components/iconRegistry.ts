export const commonLucideIconNames = Object.keys(iconRegistry).sort((a, b) => a.localeCompare(b));

const kebabToPascal = (kebabName: string): string => {
  return kebabName
    .split('-')
    .filter(Boolean)
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join('');
};

export const normalizeLucideIconName = (rawName: string): keyof typeof iconRegistry | '' => {
  let name = rawName.trim();
  if (!name) return '';

  try {
    const parsed = new URL(name);
    const iconSlug = parsed.pathname.split('/').filter(Boolean).pop();
    if (parsed.hostname.endsWith('lucide.dev') && iconSlug) {
      name = iconSlug;
    }
  } catch {
    // Not a URL; continue with the raw icon name.
  }

  const withoutIconSuffix = name.replace(/Icon$/i, '');
  const delimiterNormalizedName = withoutIconSuffix.replace(/[\s_]+/g, '-');
  const compactName = withoutIconSuffix.replace(/[\s_-]+/g, '').toLowerCase();
  const compactMatch = Object.keys(iconRegistry).find(iconName => iconName.toLowerCase() === compactName);

  const candidates = [
    name,
    withoutIconSuffix,
    delimiterNormalizedName,
    delimiterNormalizedName.includes('-') ? kebabToPascal(delimiterNormalizedName) : delimiterNormalizedName,
    withoutIconSuffix.charAt(0).toUpperCase() + withoutIconSuffix.slice(1),
    compactMatch || '',
  ];

  for (const candidate of candidates) {
    if (candidate in iconRegistry) {
      return candidate as keyof typeof iconRegistry;
    }
  }

  return '';
};

export const resolveLucideIcon = (rawName: string): LucideIcon | null => {
  const normalizedName = normalizeLucideIconName(rawName);
  return normalizedName ? iconRegistry[normalizedName] : null;
};

export const fallbackIcon = Link;
